    private void botonAgregarPersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarPersonaActionPerformed
        // Obtener los datos de los campos de texto y del JComboBox
        String rfc = RFC.getText();
        String nombreCompleto = Nombre.getText(); // Suponiendo que el campo Nombre contiene el nombre completo
        String[] partesNombre = nombreCompleto.split(" ");
        String nombre = "";
        String apellidoPaterno = "";
        String apellidoMaterno = "";
        String CURP = curp.getText();

        if (partesNombre.length >= 2) {
            nombre = partesNombre[0] + " " + partesNombre[1]; // Concatenar los dos primeros nombres
        }
        if (partesNombre.length >= 3) {
            apellidoPaterno = partesNombre[2]; // Suponiendo que la tercera parte es el apellido paterno
        }
        if (partesNombre.length >= 4) {
            apellidoMaterno = partesNombre[3]; // Suponiendo que la cuarta parte es el apellido materno
        }

        String EntidadNacimiento = curp.getText();
        Date fechaNacimiento = null;
        try {
            fechaNacimiento = new SimpleDateFormat("yyyy-MM-dd").parse(Nacimiento1.getText()); // Suponiendo que Nacimiento1 es un JTextField para la fecha
        } catch (ParseException ex) {
            // Manejar la excepci√≥n si la entrada no es un formato de fecha v√°lido
            ex.printStackTrace();
        }

        String telefono = Telefono1.getText();

      
        // Crear una instancia de Persona con los datos obtenidos, incluyendo la CURP
        Persona persona = new Persona(rfc, nombre, apellidoPaterno, apellidoMaterno, fechaNacimiento, telefono, CURP);

        // Crear una instancia de PersonaDAO
        PersonaDAO personaDAO = new PersonaDAO();

        // Guardar la nueva persona en la base de datos
        personaDAO.guardarPersona(persona);
    }//GEN-LAST:event_botonAgregarPersonaActionPerformed

